# 2. Для натурального n создать словарь индекс-значение, состоящий из элементов последовательности 3n + 1.
    
#     *Пример:*
    
#     - Для n = 6: {1: 4, 2: 7, 3: 10, 4: 13, 5: 16, 6: 19}

numberN = int(input('enterN: '))
dic_of_fun = {}

for i in range (1, numberN+1):
    dic_of_fun[i] = 3 * i + 1

print(dic_of_fun)